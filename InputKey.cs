using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Reflection;

namespace NKG
{
    //********************************************************************************
    //【構造体】入力キー
    //********************************************************************************
    [Serializable]
    public struct InputKey
    {
        //********************************************************************************
        //【定数】
        //********************************************************************************
        //最大キー数
        private int NUM_KEYS = 30;

        //********************************************************************************
        //【フィールド】
        //********************************************************************************
        //キー番号
        private int _value;

        //********************************************************************************
        //【コンストラクタ】
        //keyNumber：キー番号
        //********************************************************************************
        public InputKey(int keyNumber)
        {
            try
            {
                //============================================================
                //【引数の例外処理】
                //============================================================
                if (keyNumber < 0)
                {
                    keyNumber = 0;
                }
                else if (keyNumber >= NUM_KEYS)
                {
                    keyNumber = NUM_KEYS - 1;
                }

                //============================================================
                //【フィールドに値を設定する】
                //============================================================
                _value = keyNumber;
            }
            catch (Exception ex)
            {
                ErrorMessage.Show(ex, Assembly.GetExecutingAssembly(), MethodBase.GetCurrentMethod());
                _value = 0;
            }
        }
    }
}
